#include <iostream>
using namespace std;

int main() {
    int x, y, z, a, b, c;
    // 3 numere întregi citite/ afişate unul după altul 
    cout << "Introdu numerele x, y, z: ";
    cin >> x >> y >> z;
    cout << x << y << z << endl;

    // 3 numere întregi citite separat, cu spaţiu între ele 
    cout << "Introdu 3 numbere: \n";
    cout << "a = ";
    cin >> a;
    cout << "b = ";
    cin >> b;
    cout << "c = ";
    cin >> c;
    cout<< a << " " << b << " " << c << " \n";

    //  afișez valoarea din variabila b 
    cout << "valoarea din b este " << b;

    return 0;
}
